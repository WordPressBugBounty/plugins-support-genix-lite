    public function get_domain()
    {
        if (function_exists("site_url")) {
            return site_url();
        }

        if (defined("WPINC") && function_exists("get_bloginfo")) {
            return home_url();
        } else {
            $base_url = ((isset($_SERVER['HTTPS']) && $_SERVER['HTTPS'] == "on") ? "https" : "http");
            $base_url .= "://" . $_SERVER['HTTP_HOST'];
            $base_url .= str_replace(basename($_SERVER['SCRIPT_NAME']), "", $_SERVER['SCRIPT_NAME']);

            return $base_url;
        }
    }

    public function generate_secret_key()
    {
        $random_key = md5(wp_rand(10, 99) . wp_rand(10, 99) . time() . wp_rand(10, 99));
        $secret_key = substr($random_key, 0, 8) . "-" . substr($random_key, 8, 8) . "-" . substr($random_key, 16, 8) . "-" . substr($random_key, 24, 8);

        return $secret_key;
    }

    public function SetFromPostData($isNew = false, $data = null)
    {
        $newData = [];

        $title = sanitize_text_field(ApbdWps_PostValue('title', ''));
        $connected_email = sanitize_email(ApbdWps_PostValue('connected_email', ''));
        $send_from = sanitize_text_field(ApbdWps_PostValue('send_from', ''));
        $from_email = sanitize_email(ApbdWps_PostValue('from_email', ''));
        $use_title = sanitize_text_field(ApbdWps_PostValue('use_title', ''));
        $attach_with = sanitize_text_field(ApbdWps_PostValue('attach_with', ''));
        $choose_category = absint(ApbdWps_PostValue('choose_category', ''));
        $privacy_policy = sanitize_text_field(ApbdWps_PostValue('privacy_policy', ''));
        $status = sanitize_text_field(ApbdWps_PostValue('status', ''));

        $send_from = in_array($send_from, array("C", "D")) ? $send_from : "C";
        $use_title = 'Y' === $use_title ? 'Y' : 'N';
        $attach_with = 'A' === $attach_with ? 'A' : 'I';
        $choose_category = strval($choose_category);
        $privacy_policy = 'Y' === $privacy_policy ? 'Y' : 'N';
        $status = 'A' === $status ? 'A' : 'I';

        $from_email = '';

        if (
            (1 > strlen($title)) ||
            (1 > strlen($connected_email))
        ) {
            return;
        }

        $newData['title'] = $title;
        $newData['connected_email'] = $connected_email;
        $newData['send_from'] = $send_from;
        $newData['from_email'] = $from_email;
        $newData['use_title'] = $use_title;
        $newData['attach_with'] = $attach_with;
        $newData['privacy_policy'] = $privacy_policy;
        $newData['status'] = $status;

        if ('A' === $attach_with) {
            if (empty($choose_category)) {
                return;
            }

            $newData['choose_category'] = $choose_category;
        }

        if ('Y' !== $privacy_policy) {
            $this->AddError("You must agree with the email to ticket terms and conditions.");
            return;
        }

        if ($isNew) {
            $is_duplicate = self::CheckDuplicate(null, $connected_email, $status);

            if (!$is_duplicate) {
                $secret_key = $this->generate_secret_key();

                if (! empty($email)) {
                    $newData['secret_key'] = $secret_key;
                } else {
                    $this->AddError("Something went wrong.");
                    return;
                }
            }
        }

        return parent::SetFromPostData($isNew, $newData);
    }

