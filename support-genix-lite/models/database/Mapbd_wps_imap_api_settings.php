    static function CheckDuplicate($mailboxObj, $connected_email = '', $status = '', $id = 0)
    {
        $is_duplicate = false;

        $mailbox_id = absint($id);
        $mailbox_email = '';
        $mailbox_status = '';

        if (!empty($mailboxObj)) {
            $mailbox_email = sanitize_email($mailboxObj->connected_email);
            $mailbox_status = sanitize_text_field($mailboxObj->status);
        } elseif (!empty($connected_email)) {
            $mailbox_email = sanitize_email($connected_email);
            $mailbox_status = sanitize_text_field($status);
        }

        if (
            !empty($mailbox_email) &&
            ('A' === $mailbox_status)
        ) {
            $mainObj = new Mapbd_wps_imap_api_settings();
            $mainObj->connected_email($mailbox_email);
            $mainObj->id("!= '{$mailbox_id}'", true);
            $mainObj->status('A');

            if ($mainObj->Select()) {
                $is_duplicate = true;
            } else {
                $mainObj = new Mapbd_wps_imap_settings();
                $mainObj->user_email($mailbox_email);
                $mainObj->id("!= '{$mailbox_id}'", true);
                $mainObj->status('A');

                if ($mainObj->Select()) {
                    $is_duplicate = true;
                }
            }
        }

        return $is_duplicate;
    }
